    RST = '\033[0m'

    def print_phone(lst: list, clmn: int = 4, clmn_width: int = 13):
        """ вывод в виде таблицы с количеством колонок = clmn """
        print("-" * (2 * clmn_width + 6) * clmn)
        print(f'{"abonents id":>{clmn_width}}{"abonents phone":>{clmn_width + 3}}  |' * clmn)
        print("-" * (2 * clmn_width + 6) * clmn)
        lenlst = len(lst)
        rows = round(lenlst / clmn + 0.4)
        for i in range(0, rows):
            for j in range(clmn):
                ind = rows * j + i
                if ind < lenlst:
                    print(f'{lst[ind][0]:>{clmn_width}}{lst[ind][1]:>{clmn_width + 3}}', end="  |")
                else:
                    print(f'{"":{clmn_width * 2 + 3}}', end="  |")
            print()

    # создание списков id, phones абонентов
